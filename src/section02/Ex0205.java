package section02;

public class Ex0205 {
  public static void main(String[] args) {
    int intNum = 100;
    System.out.println("intNum : " + intNum);

    long longNum = 10000;
    System.out.println("longNum : " + longNum);
    // L을 안붙이면 int타입으로 인식
    long longNum2 = 100000000L;
    System.out.println("longNum : " + longNum2);

  }  

}
