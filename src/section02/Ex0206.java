package section02;

public class Ex0206 {
  public static void main(String[] args) {
    // 실수형
    // float, double
    float floatPi = 3.1414592653589783f;
    // F를 뒤에 안붙이면 더블파입으로 인식
    double doublePi = 3.1414592653589783D;

    System.out.println("floatpi : " + floatPi);
    System.out.println("doublePI: " + doublePi);
  }
}
