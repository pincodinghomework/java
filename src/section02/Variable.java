package section02;

public class Variable {
    public static void  main(String[] args){
        int a; // * 변수 선언
        a = 3; // * a에 3을 할당(대입 : assignment) : 초기화
        // ? 이걸 한번에 선언할수 있음

        // * 변수(Variable) : 변할 수 있는 수
        a = 10;
        
        int b = 5; // * 변수 정의

        System.out.println(a);
        System.out.println(b);
        // * System.out.println(); : sout 자동 완성 
    }
    
}
