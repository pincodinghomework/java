package section03;

public class Ex0301 {
  public static void main(String[] args) {
    int num = 100; 
    int resultPlus = +num;
    int resultMinus = -num;
    System.out.println("resultPlust\t : " + resultPlus);
    System.out.println("resultMinus\t : " + resultMinus);
    // 띄어쓰기 \t
    System.out.println("resultPlust\t : " + -resultPlus);
    System.out.println("resultMinus\t : " + -resultMinus);
  }
}
